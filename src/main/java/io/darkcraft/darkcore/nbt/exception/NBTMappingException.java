package io.darkcraft.darkcore.nbt.exception;

public class NBTMappingException extends NBTException
{
	private static final long serialVersionUID = 1L;

	public NBTMappingException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public NBTMappingException(String arg0)
	{
		super(arg0);
	}

	public NBTMappingException(Throwable arg0)
	{
		super(arg0);
	}

}
